package kaede.me.howoldrobot.analyse.model;

public class FaceRectangle{
    public int left;
    public int top;
    public int width;
    public int height;

    @Override
    public String toString() {
        return "FaceRectangle{" +
                "left=" + left +
                ", top=" + top +
                ", width=" + width +
                ", height=" + height +
                '}';
    }
}
